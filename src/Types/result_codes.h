#ifndef _RESULT_CODES_QUILBOARS_GUARD
#define _RESULT_CODES_QUILBOARS_GUARD

#define FAILED(result_code) (result_code) != Result::NO_ERROR

enum class Result {
	NO_ERROR = 0x0000,
	NULL_POINTER = 0x0001,
	INVALID_ARGUMENT = 0x0002,

	LOGIN_COLLISION = 0x0101,
	WRONG_PASSWORD = 0x0102,
	SKIP = 0x105,

	DB_ERROR = 0x110,
	INVALID_ID = 0x111
};

#endif //_RESULT_CODES_QUILBOARS_GUARD